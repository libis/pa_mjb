<div class="row" style="text-align: justify; text-justify: inter-word;">
	<div class="col-sm-8">
		<p>The Chinese Christian Texts Database (CCT-Database) is a research database of primary and secondary sources concerning the cultural contacts between China and Europe in the seventeenth and eighteenth centuries (from 1582 to ca. 1840). The cultural contacts comprise documents in the various fields of cultural interaction: religion, philosophy, science, art, etc.</p>
	</div>
	<div class="col-sm-8">
		<p>
			<b><H6>History</H6></b>
			It was the late Professor Erik Zürcher (1928-2008) who took the initiative of compiling a bibliographical list of Chinese primary sources concerning Christianity in China in the seventeenth century. Since the 1980s, Ad Dudink and Nicolas Standaert have continued and elaborated the project and converted the data thus far collected into an online research database. In that form it will be an on-going project, which makes it possible to continually add new sources and to update, revise or expand the description of sources already included.
		</p>
	</div>
	<div class="col-sm-8">
		<p>
			<b><H6>Structure</H6></b>
			The database is set up as a research database, both in regard to its content and search possibilities. The description of primary sources, divided into Chinese and European primary sources, is intended as to be extensive and to allow for multiple links. In addition to the usual bibliographical references such as title, author, impressum or reprints, these references include: a content description, the mentioning of call numbers in major library collections, references to translations or secondary sources, and various notes on the author or text history. The database is multi-lingual in the sense that there are references to documents and publications in a wide variety of ancient and modern Asian and European languages. The primary and secondary sources are given a thematic category, which basically follows the categorisation of the Handbook of Christianity in China : Volume One (635-1800) , Leiden : Brill, 2000. (link: http://www.brill.nl/default.aspx?partid=18&pid=8857). For an overview of the categories, click “Categories” on the left navigation bar of this page.
		</p>
	</div>
	<div class="col-sm-8">
		<p>
			<b><H6>How to use?</H6></b>
			The database is divided in "primary" and "secondary" sources. The primary sources are ca. 1050 Chinese texts documents dating from roughly the seventeenth and eighteenth centuries. They include printed books, manuscripts, pamphlets and maps. There are over 4000 secondary sources, which, if applicable, are linked to the primary sources. Searches can be made in the whole database, or in the primary or secondary sources separately. As much as possible the data are linked to the original sources, in so far these appear on public websites (notice that sometimes the address changes and the link is interrupted).
		</p>
	</div>
